public class CheckAuthorizedAccountIPRequest implements Alignable {
    private String authorizedIp;

    @RequestField(name="authorized_ip")
    /**
    * The IP4 to test.
    */
    public String getAuthorizedIp() {
        return this.authorizedIp;
    }

    public boolean hasAuthorizedIp() {
        return this.authorizedIp != null;
    }

    /**
    * The IP4 to test.
    */
    public CheckAuthorizedAccountIPRequest setAuthorizedIp(String d) {
        this.authorizedIp = d;
        return this;
    }

    public String toString(int alignment) {
        char[] preAligned = new char[alignment - 1];
        char[] aligned = new char[alignment];
        Arrays.fill(preAligned, '\t');
        Arrays.fill(aligned, '\t');
        StringBuilder sb = new StringBuilder()
            .append(preAligned)
            .append('{')
            .append(System.lineSeparator());
        if (authorizedIp != null) {
            sb.append(aligned)
                .append("\"authorizedIp\": \"")
                .append(authorizedIp)
                .append('"')
                .append(',')
                .append(System.lineSeparator());
        }
        return sb.append(preAligned).append('}').append(',').toString();
    }

    @Override
    public String toString() {
        return toString(1);
    }}